export function getChecklistTemplate(mode: TransportMode): ChecklistGroup[] {
  return checklistTemplates[mode].map((group) => ({
    ...group,
    items: group.items.map((item) => ({
      ...item,
      value: null as ChecklistValue,
      meetsStandard: false,
      note: '',
      photos: [],
      flagged: false,
    })),
  }))
}

export function getTransportLabel(station: Station): string {
  if (station.mode === 'ทางราง' && station.railSubtype) return station.railSubtype
  return station.mode
}

